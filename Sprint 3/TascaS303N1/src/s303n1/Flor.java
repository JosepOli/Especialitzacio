package s303n1;

public class Flor {

	private String color;
	private double preu;

	public Flor(String color, double preu) {
		super();
		this.color = color;
		this.preu = preu;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public double getPreu() {
		return preu;
	}

	public void setPreu(double preu) {
		this.preu = preu;
	}

}
